export type Translations = typeof translations.uz;

interface LangCtx {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: Translations;
}

const Ctx = createContext<LangCtx>({
  lang: "uz",
  setLang: () => {},
  t: translations.uz,
});

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    const saved = localStorage.getItem("nn-lang") as Lang | null;
    if (saved === "uz" || saved === "ru" || saved === "en") return saved;
    return "uz";
  });

  const setLang = (l: Lang) => {
    setLangState(l);
    localStorage.setItem("nn-lang", l);
  };

  return (
    <Ctx.Provider value={{ lang, setLang, t: translations[lang] as unknown as Translations }}>
      {children}
    </Ctx.Provider>
  );
}

export const useLang = () => useContext(Ctx);

